    private void cargarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cargarActionPerformed
        // TODO add your handling code here:
        if (fiche == true) {
            String f = (String) Fichero.getSelectedItem() + ".tsp";
            mf = new Lector(f);
            mf.leeFichero(vector);
        } else if (ficheno == true) {
            int tama = Integer.parseInt(VectorTam.getText());
            Random rand = new Random();
            for (int i = 0; i < tama; i++) {
                Puntos paux = new Puntos(i, rand.nextFloat(), rand.nextFloat());
                vector.add(paux);
            }
        }
        Kruskal.setEnabled(true);
        Prim.setEnabled(true);
        DYV.setEnabled(true);
        BE.setEnabled(true);
    }//GEN-LAST:event_cargarActionPerformed

    private void BEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BEActionPerformed
        // TODO add your handling code here:
        consola.setText("");
        ex = new Perimetros();
        tini = System.currentTimeMillis();
        float tam = ex.Busqueda(vector, vectorSol, 0);
        tfin = System.currentTimeMillis();
        String s = "Tiempo total : \t" + (tfin - tini);
        consola.append("Busqueda Exhaustiva \n");
        consola.append(s + "\n");
        consola.append("Perimetro: \t" + tam);
        fGNU = new FicheroGNU();
        try {
            fGNU.Comandos();
            fGNU.Conjunto(vector);
            fGNU.Solucion(vectorSol);
        } catch (IOException ex) {
            Logger.getLogger(Menu.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_BEActionPerformed

    private void DYVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DYVActionPerformed
        // TODO add your handling code here:
        consola.setText("");
        HeapSort heap = new HeapSort();
        vectorSol.clear();
        DivVenc dv = new DivVenc(vector);
        fGNU = new FicheroGNU();
        tini = System.currentTimeMillis();
        heap.ordenaMonticulos(vector);
        vectorSol = dv.triangulo(vector);
        float sol = dv.per();
        tfin = System.currentTimeMillis();
        String s = "Tiempo total : \t" + (tfin - tini);
        consola.append("Divide y Venceras \n");
        consola.append(s + "\n");
        consola.append("Perimetro: \t" + sol);
        try {
            fGNU.Comandos();
            fGNU.Conjunto(vector);
            fGNU.Solucion(vectorSol);
        } catch (IOException ex) {
            Logger.getLogger(Menu.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_DYVActionPerformed

